# QUE PASA A FONDO CUANDO SE EJECUTA UN PROGRAMA EN PYTHON

# Cuando ejecuta el archivo hello_world.py, la terminación .py indica que
# el archivo es un programa Python. Luego, el editor ejecuta el archivo a través del
# Intérprete de Python, que lee el programa y determina qué
# significa cada palabra en el programa. Por ejemplo, cuando el intérprete ve
# la palabra print, imprime en pantalla lo que está dentro de los paréntesis.
# A medida que escribe sus programas, su editor resalta diferentes partes de
# su programa de diferentes maneras. Por ejemplo, reconoce que la impresión es el
# nombre de una función y muestra esa palabra en un color determinado. Reconoce que “Hola
# ¡Mundo python!” no es código de Python y muestra esa frase en otro color determinado. Esta
# característica se llama resaltado de sintaxis y es bastante útil cuando comienza a escribir
# sus propios programas.
