#Quita el espacio en blanco del lado derecho con el metodo rstrip()
favorite_language = 'python  '
favorite_language = favorite_language.rstrip()
print(favorite_language)

#Quita el espacio en blanco del lado izquierdo con el metodo lstrip()
favorite_language = favorite_language.lstrip()
print(favorite_language)

#Quita espacio en blanco de ambas lados con el metodo strip()
favorite_language = '    python   '
favorite_language = favorite_language.strip()
print(favorite_language)
