a = 4
b = 2

#esto evalua y (a) es exactamente igual a (b)
resultado = a == b
print(f'resultado == : {resultado}')

#evalua si (a) es diferente de (b)
resultado = a != b
print(f'resultado != : {resultado}')

#evalua si (a) es mayor que (b)
resultado = a > b
print(f'Resultado > : {resultado}')

#evalua si (a) es mayor igual que (b)
resultado = a >= b
print (f'Resultado >= :{resultado}')

#evalua si (a) menor que (b)
resultado = a < b
print(f'Resultado < : {resultado}')

#evalua si (a) es menor o igual que (b)
resultado = a <= b
print(f'Resultado <= : {resultado}')

