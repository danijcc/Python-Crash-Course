#los operadores logicos son (and,or,not) permite comparar expreciones booleanas

#and devuelve True si ambos operadores son true (a and b)
a = True
b = True
resultado = a and b
print(resultado)

#or devuelve True si alguno de los operadores es True a or b
resultado = a or b
print(resultado)

#not devuelve True si alguno de los operandos son falsos basicamente invierte el valor de la exprecion
resultado = not a
print(resultado)